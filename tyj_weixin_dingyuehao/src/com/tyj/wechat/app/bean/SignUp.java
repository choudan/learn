public class SignUp {
	int points;
	int pointsIncrease;
	String content;
	/**
	 * @return the points
	 */
	public int getPoints() {
		return points;
	}
	/**
	 * @param points the points to set
	 */
	public void setPoints(int points) {
		this.points = points;
	}
	/**
	 * @return the pointsIncrease
	 */
	public int getPointsIncrease() {
		return pointsIncrease;
	}
	/**
	 * @param pointsIncrease the pointsIncrease to set
	 */
	public void setPointsIncrease(int pointsIncrease) {
		this.pointsIncrease = pointsIncrease;
	}
	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}
	/**
	 * @param content the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}
}
